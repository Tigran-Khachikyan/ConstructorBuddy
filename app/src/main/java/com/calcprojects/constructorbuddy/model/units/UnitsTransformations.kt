package com.calcprojects.constructorbuddy.model.units

fun fromKGToPound(value: Double) = 2.205 * value
fun fromPoundToKG(value: Double) = value / 2.205

fun fromCMToInch(value: Double) = 0.3937 * value
fun fromInchToCM(value: Double) = value / 0.3937

fun fromGCm3ToLbIn3(value: Double) = 0.036 * value
fun fromLbIn3ToGCm3(value: Double) = value / 2.205